<h1>Using INFO</h1>

<p>After defining our namespace and database, there's a way to retrieve the those informations.</p>

<pre class="mb-0">INFO FOR KV;</pre>

<pre class="mb-0">INFO FOR NS;</pre>
<p class="mt-0">This will show the lists of databases.</p>

<pre class="mb-0">INFO FOR DB;</pre>
<p class="mt-0">
	The above query will show the detail of your database, such as tables, scopes and more.
</p>
