<h1>What is SurrealDB?</h1>

<p>
	To summarize what SurrealDB can do, it is a combination of Relational Database "without joins";
	Document "nosql"; KeyValue such as Redis - into one single database, and a way to programmatically
	inject an Javascript code to handle the queries such as loops and variables.
</p>

<p>Think of it as a way to normalize directly your data sets without writing a backend code.</p>
